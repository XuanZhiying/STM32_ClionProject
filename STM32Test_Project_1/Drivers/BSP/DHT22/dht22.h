#ifndef __DHT22_H
#define __DHT22_H

#include <stdint.h>
#include "./SYSTEM/sys/sys.h"

/******************************************************************************************/
/* DHT22 GPIO 引脚定义                                                                    */
/* PA8, 推挽输出/浮空输入切换, 外部上拉 4.7KΩ 至 3.3V                                    */
/******************************************************************************************/
#define DHT22_GPIO_PORT                 GPIOB
#define DHT22_GPIO_PIN                  GPIO_PIN_11
#define DHT22_GPIO_CLK_ENABLE()         do{ __HAL_RCC_GPIOB_CLK_ENABLE(); }while(0)

/******************************************************************************************/
/* 返回值定义                                                                             */
/******************************************************************************************/
#define DHT_OK                          0       /* 读取成功 */
#define DHT_CHECKSUM_ERROR              -1      /* 校验和错误 */
#define DHT_TIMEOUT_ERROR               -2      /* 传感器无响应超时 */
#define DHT_VALUE_ERROR                 -3      /* 数值超出合理范围 */

/******************************************************************************************/
/* 函数声明                                                                               */
/******************************************************************************************/
void dht22_init(void);                                  /* GPIO 初始化 */
int  dht22_read(float *temperature, float *humidity, uint8_t *raw_data);   /* 读取温湿度, raw_data[5] 出参可 NULL */

#endif /* __DHT22_H */
